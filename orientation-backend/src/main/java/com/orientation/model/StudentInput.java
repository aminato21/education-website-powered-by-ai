package com.orientation.model;

public class StudentInput {
    private double mathScore;
    private double physicsScore;
    private double chemistryScore;
    private double biologyScore;
    private double englishScore;
    private double geographyScore;
    private double weeklySelfStudyHours;
    private double absenceDays;

    // Getters and Setters
    public double getMathScore() { return mathScore; }
    public void setMathScore(double mathScore) { this.mathScore = mathScore; }

    public double getPhysicsScore() { return physicsScore; }
    public void setPhysicsScore(double physicsScore) { this.physicsScore = physicsScore; }

    public double getChemistryScore() { return chemistryScore; }
    public void setChemistryScore(double chemistryScore) { this.chemistryScore = chemistryScore; }

    public double getBiologyScore() { return biologyScore; }
    public void setBiologyScore(double biologyScore) { this.biologyScore = biologyScore; }

    public double getEnglishScore() { return englishScore; }
    public void setEnglishScore(double englishScore) { this.englishScore = englishScore; }

    public double getGeographyScore() { return geographyScore; }
    public void setGeographyScore(double geographyScore) { this.geographyScore = geographyScore; }

    public double getWeeklySelfStudyHours() { return weeklySelfStudyHours; }
    public void setWeeklySelfStudyHours(double weeklySelfStudyHours) { this.weeklySelfStudyHours = weeklySelfStudyHours; }

    public double getAbsenceDays() { return absenceDays; }
    public void setAbsenceDays(double absenceDays) { this.absenceDays = absenceDays; }
}
